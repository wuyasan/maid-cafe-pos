def _do_upload(client, stored_key="menu-items/abc123.png", data=_TINY_PNG,
               content_type="image/png"):
    """POST to /admin/uploads/menu-image with save_image mocked."""
    with patch(
        "app.api.v1.endpoints.uploads.save_image",
        return_value=stored_key,
    ), patch(
        "app.api.v1.endpoints.uploads.storage_backend",
        return_value="local",
    ):
        resp = client.post(
            "/api/v1/admin/uploads/menu-image",
            files={"image": ("test.png", BytesIO(data), content_type)},
        )
    return resp


# ---------------------------------------------------------------------------
# [P4] URL shape: default relative path; absolute when PUBLIC_UPLOAD_BASE_URL set
# ---------------------------------------------------------------------------

class TestLocalUploadUrl:
    """Returned image_url must be a relative /uploads/ path by default."""

    def test_upload_url_is_relative_by_default(self, client):
        """Without PUBLIC_UPLOAD_BASE_URL, image_url is a relative path."""
        env = {k: v for k, v in os.environ.items() if k != "PUBLIC_UPLOAD_BASE_URL"}
        with patch.dict(os.environ, env, clear=True):
            resp = _do_upload(client)
        assert resp.status_code == 200, resp.text
        url = resp.json()["image_url"]
        assert url.startswith("/uploads/"), (
            f"Expected relative /uploads/ path, got: {url!r}"
        )
        assert "://" not in url, (
            f"Expected relative path (no scheme), got: {url!r}"
        )

    def test_upload_url_contains_stored_key(self, client):
        stored_key = "menu-items/deadbeef.png"
        env = {k: v for k, v in os.environ.items() if k != "PUBLIC_UPLOAD_BASE_URL"}
        with patch.dict(os.environ, env, clear=True):
            resp = _do_upload(client, stored_key=stored_key)
        assert resp.status_code == 200, resp.text
        url = resp.json()["image_url"]
        assert url.endswith(stored_key), (
            f"Expected URL to end with {stored_key!r} but got: {url!r}"
        )

    def test_upload_url_no_double_slash_in_path(self, client):
        env = {k: v for k, v in os.environ.items() if k != "PUBLIC_UPLOAD_BASE_URL"}
        with patch.dict(os.environ, env, clear=True):
            resp = _do_upload(client)
        assert resp.status_code == 200, resp.text
        url = resp.json()["image_url"]
        assert "//" not in url, (
            f"URL must not contain '//': {url!r}"
        )

    def test_upload_url_absolute_when_public_base_url_set(self, client):
        """When PUBLIC_UPLOAD_BASE_URL is set, image_url is an absolute URL."""
        with patch.dict(os.environ, {"PUBLIC_UPLOAD_BASE_URL": "https://cdn.example.com"}):
            resp = _do_upload(client)
        assert resp.status_code == 200, resp.text
        url = resp.json()["image_url"]
        assert url.startswith("https://cdn.example.com/uploads/"), (
            f"Expected absolute CDN URL, got: {url!r}"
        )

    def test_upload_url_public_base_url_trailing_slash_normalized(self, client):
        """PUBLIC_UPLOAD_BASE_URL with trailing slash must not produce double-slash."""
        with patch.dict(os.environ, {"PUBLIC_UPLOAD_BASE_URL": "https://cdn.example.com/"}):
            resp = _do_upload(client, stored_key="menu-items/img.png")
        assert resp.status_code == 200, resp.text
        url = resp.json()["image_url"]
        assert url == "https://cdn.example.com/uploads/menu-items/img.png", (
            f"Double-slash or wrong URL: {url!r}"
        )


