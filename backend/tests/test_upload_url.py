class TestImageByteValidation:
    """Upload must be rejected when Pillow cannot fully decode the image."""

    def test_real_png_bytes_accepted(self, client):
        """Genuine PNG (Pillow-generated) → 200."""
        resp = _do_upload(client, data=_TINY_PNG, content_type="image/png")
        assert resp.status_code == 200, resp.text

    def test_real_jpeg_bytes_accepted(self, client):
        """Genuine JPEG (Pillow-generated) → 200."""
        resp = _do_upload(client, data=_TINY_JPEG, content_type="image/jpeg")
        assert resp.status_code == 200, resp.text

    def test_non_image_bytes_declared_as_png_rejected_400(self, client):
        """Python source bytes declared as image/png must be rejected with 400."""
        with patch(
            "app.api.v1.endpoints.uploads.storage_backend",
            return_value="local",
        ):
            resp = client.post(
                "/api/v1/admin/uploads/menu-image",
                files={
                    "image": ("evil.py", BytesIO(_FAKE_PY_AS_PNG), "image/png")
                },
            )
        assert resp.status_code == 400, resp.text
        detail = resp.json()["detail"].lower()
        assert "image" in detail or "format" in detail, (
            f"Unexpected detail: {resp.json()['detail']!r}"
        )

    def test_text_file_declared_as_jpeg_rejected_400(self, client):
        """Plain text declared as image/jpeg → 400."""
        fake_text = b"Hello, I am not a JPEG.\n"
        with patch(
            "app.api.v1.endpoints.uploads.storage_backend",
            return_value="local",
        ):
            resp = client.post(
                "/api/v1/admin/uploads/menu-image",
                files={
                    "image": ("not_a_jpeg.jpg", BytesIO(fake_text), "image/jpeg")
                },
            )
        assert resp.status_code == 400, resp.text

    def test_corrupt_png_with_magic_header_rejected_400(self, client):
        """PNG magic header followed by garbage must be rejected with 400 by Pillow."""
        with patch(
            "app.api.v1.endpoints.uploads.storage_backend",
            return_value="local",
        ):
            resp = client.post(
                "/api/v1/admin/uploads/menu-image",
                files={
                    "image": ("corrupt.png", BytesIO(_CORRUPT_PNG), "image/png")
                },
            )
        assert resp.status_code == 400, resp.text
        detail = resp.json()["detail"].lower()
        assert "image" in detail or "format" in detail, (
            f"Unexpected detail: {resp.json()['detail']!r}"
        )

    def test_empty_file_rejected_400(self, client):
        """Empty payload → 400 (existing check, still passes)."""
        with patch(
            "app.api.v1.endpoints.uploads.storage_backend",
            return_value="local",
        ):
            resp = client.post(
                "/api/v1/admin/uploads/menu-image",
                files={
                    "image": ("empty.png", BytesIO(b""), "image/png")
                },
            )
        assert resp.status_code == 400, resp.text

    def test_unexpected_exception_propagates_not_swallowed_as_400(self):
        """An unexpected error in _validate_and_get_content_type must NOT be caught
        and converted to 400.  We verify this by calling the function directly and
        confirming the non-decode exception is NOT converted to HTTPException 400."""
        from fastapi import HTTPException
        from app.api.v1.endpoints.uploads import _validate_and_get_content_type
        from unittest.mock import patch as _patch

        # Patch PIL Image.open to raise an unexpected RuntimeError (not in _DECODE_ERRORS).
        with _patch("app.api.v1.endpoints.uploads.Image.open", side_effect=RuntimeError("unexpected internal error")):
            try:
                _validate_and_get_content_type(_TINY_PNG)
                assert False, "Expected an exception to be raised"
            except HTTPException as e:
                # If it's caught and re-raised as HTTPException 400, the narrow
                # except clause is still too broad — fail the test.
                assert False, (
                    f"RuntimeError was wrongly swallowed and turned into HTTPException {e.status_code}"
                )
            except RuntimeError:
                # Correct: RuntimeError propagates unmasked.
                pass
