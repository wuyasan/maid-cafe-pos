class TestLocalUploadUrlIsPublic:
    """Local upload URL must be on the public /uploads/ mount, not /api/v1/."""

    def test_upload_url_not_under_api_v1(self, client):
        """image_url must NOT contain /api/v1/ — that path requires gateway auth."""
        env = {k: v for k, v in os.environ.items() if k != "PUBLIC_UPLOAD_BASE_URL"}
        with patch.dict(os.environ, env, clear=True):
            resp = _do_upload(client)
        assert resp.status_code == 200, resp.text
        url = resp.json()["image_url"]
        assert "/api/v1/" not in url, (
            f"URL is under gateway-gated /api/v1/ path: {url!r}"
        )

    def test_upload_url_starts_with_uploads_prefix(self, client):
        """image_url path must start with /uploads/ (public static mount)."""
        env = {k: v for k, v in os.environ.items() if k != "PUBLIC_UPLOAD_BASE_URL"}
        with patch.dict(os.environ, env, clear=True):
            resp = _do_upload(client)
        assert resp.status_code == 200, resp.text
        url = resp.json()["image_url"]
        assert url.startswith("/uploads/"), (
            f"Expected path to start with '/uploads/' but got: {url!r}"
        )

    def test_maid_upload_url_starts_with_uploads_prefix(self, client):
        """Maid image upload also returns a /uploads/ URL (not /api/v1/)."""
        stored_key = "maids/cafebabe.jpg"
        env = {k: v for k, v in os.environ.items() if k != "PUBLIC_UPLOAD_BASE_URL"}
        with patch.dict(os.environ, env, clear=True):
            with patch(
                "app.api.v1.endpoints.uploads.save_image",
                return_value=stored_key,
            ), patch(
                "app.api.v1.endpoints.uploads.storage_backend",
                return_value="local",
            ):
                resp = client.post(
                    "/api/v1/admin/uploads/maid-image",
                    files={"image": ("test.jpg", BytesIO(_TINY_JPEG), "image/jpeg")},
                )
        assert resp.status_code == 200, resp.text
        url = resp.json()["image_url"]
        assert url.startswith("/uploads/"), (
            f"Maid image URL not public: {url!r}"
        )
        assert "/api/v1/" not in url, (
            f"Maid image URL is under gateway-gated /api/v1/: {url!r}"
        )


